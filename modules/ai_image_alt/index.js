/**
 * Parse JSON request body
 */
function parseJsonBody(req) {
  return new Promise((resolve, reject) => {
    let body = '';
    req.on('data', chunk => {
      body += chunk.toString();
    });
    req.on('end', () => {
      try {
        resolve(JSON.parse(body));
      } catch (e) {
        reject(new Error('Invalid JSON'));
      }
    });
    req.on('error', reject);
  });
}

/**
 * Parse multipart form data (simplified - handles single file upload)
 */
function parseMultipartFormData(req) {
  return new Promise((resolve, reject) => {
    const boundary = req.headers['content-type']?.split('boundary=')[1];
    if (!boundary) {
      reject(new Error('No boundary found'));
      return;
    }

    const chunks = [];
    req.on('data', chunk => chunks.push(chunk));
    req.on('end', () => {
      const buffer = Buffer.concat(chunks);
      const parts = [];
      const boundaryBuffer = Buffer.from(`--${boundary}`);

      let start = 0;
      while (true) {
        const idx = buffer.indexOf(boundaryBuffer, start);
        if (idx === -1) break;

        const nextIdx = buffer.indexOf(boundaryBuffer, idx + boundaryBuffer.length);
        if (nextIdx === -1) break;

        const part = buffer.slice(idx + boundaryBuffer.length, nextIdx);
        parts.push(part);
        start = nextIdx;
      }

      const result = {};
      for (const part of parts) {
        const headerEnd = part.indexOf('\r\n\r\n');
        if (headerEnd === -1) continue;

        const headers = part.slice(0, headerEnd).toString();
        const data = part.slice(headerEnd + 4, part.length - 2);

        const nameMatch = headers.match(/name="([^"]+)"/);
        const filenameMatch = headers.match(/filename="([^"]+)"/);

        if (nameMatch) {
          const name = nameMatch[1];
          if (filenameMatch) {
            result[name] = {
              filename: filenameMatch[1],
              data
            };
          } else {
            result[name] = data.toString();
          }
        }
      }

      resolve(result);
    });
    req.on('error', reject);
  });
}

