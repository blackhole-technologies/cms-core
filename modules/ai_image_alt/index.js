/**
 * ai_image_alt/index.js - AI-Powered Image Alt Text Generation
 *
 * WHY THIS EXISTS:
 * Automatically generates accessible, descriptive alt text for images using AI providers.
 * Improves web accessibility compliance and reduces manual content entry burden.
 *
 * DESIGN DECISIONS:
 * - Integrates with AI registry to use configured providers
 * - Supports multiple AI providers with automatic fallback
 * - Includes quality scoring to ensure alt text meets WCAG standards
 * - Provides both service API and CLI interface
 * - Stores generation metadata for analytics
 *
 * USAGE:
 *   const altTextService = services.get('ai-alt-text');
 *   const result = await altTextService.generate(imagePath);
 *   const score = await altTextService.scoreQuality(altText);
 */

import { readFileSync, existsSync } from 'node:fs';
import { basename, extname, join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

// Get the directory of this module for loading templates
const __dirname = dirname(fileURLToPath(import.meta.url));

/**
 * Load a template file from this module's templates directory
 */
function loadTemplate(name) {
  const templatePath = join(__dirname, 'templates', name);
  return readFileSync(templatePath, 'utf-8');
}

/**
 * Redirect helper
 */
function redirect(res, url) {
  res.writeHead(302, { 'Location': url });
  res.end();
}

/**
 * Parse URL-encoded form data from request body
 */
function parseFormBody(req) {
  return new Promise((resolve, reject) => {
    let body = '';

    req.on('data', chunk => {
      body += chunk.toString();
      if (body.length > 1024 * 1024) {
        reject(new Error('Form data too large'));
      }
    });

    req.on('end', () => {
      try {
        const data = {};
        const pairs = body.split('&');

        for (const pair of pairs) {
          const [key, value] = pair.split('=').map(s => decodeURIComponent(s.replace(/\+/g, ' ')));
          if (key) {
            // Handle arrays (multiple values with same key)
            if (data[key]) {
              if (Array.isArray(data[key])) {
                data[key].push(value || '');
              } else {
                data[key] = [data[key], value || ''];
              }
            } else {
              data[key] = value || '';
            }
          }
        }

        resolve(data);
      } catch (error) {
        reject(new Error('Invalid form data'));
      }
    });

    req.on('error', reject);
  });
}

